def get_pet_id(pet_data):
    pet_id = pet_data["id"]
    return pet_id


def get_pet_name(pet_data):
    pet_name = pet_data["name"]
    return pet_name


def get_pet_status(pet_data):
    pet_status = pet_data["status"]
    return pet_status


def get_pet_message(pet_data):
    pet_message = pet_data["message"]
    return pet_message
